# // Arithmetic Operators
x = 10
y = 15
print("Addition : ",x+y)
print("Subtraction : ",x-y)
print("Multiplication : ",x*y)
print("Division : ",x/y)
print("Modulas/Remainder : ",y%x)
print("Exponential : ",x**y)
print("Floor Division : ",x+y)

# //Comparison Operators or Relational Operators : 
a = 5
b = 10
print("Equal to : ",a==b)
print("Not Equal to : ",a!=b)
print("Greater than : ",a>b)
print("Less than : ",a<b)
print("Greater than or Equal to : ",a>=b)
print("Less than or Equal to : ",a<=b)

# // Logical Operators
p = True
q = False
print("Logical AND : ",p and q)
print("Logical OR : ",p or q)   
print("Logical NOT : ",not p)

# //BitWise Operators
m = 10  # In binary: 1010
n = 5   # In binary: 0101
print("Bitwise AND : ",m & n)  # Output: 0 (In binary: 0000)
print("Bitwise OR : ",m | n)   # Output: 15 (In binary: 1111)
print("Bitwise XOR : ",m ^ n)  # Output: 15 (In binary: 1111)
print("Bitwise NOT : ",~m)     # Output: -11 (In binary: 1111...    11110101)
print("Bitwise Left Shift : ",m << 2)  # Output: 40 (In binary: 101000)
print("Bitwise Right Shift : ",m >> 2) # Output: 2 (In binary: 10)                      

